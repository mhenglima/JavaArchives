
public class Extras {
	
	private String sType;
	private int nAmount;
	
	public void setType(String sType) {
		this.sType = sType;
	}
	
	public String getType() {
		return sType;
	}
	
	public void setAmount(int nAmount) {
		this.nAmount = nAmount;
	}
	
	public int getAmount() {
		return nAmount;
	}
	
}
